// ─── tier から文字プールを取得する関数 ───
function getGlyphForTier(tier, themeId) {
  const themePool = THEME_GLYPH_POOLS[themeId] || THEME_GLYPH_POOLS.yoruame;
  const dedup = (base, extra) => [...new Set([...base, ...extra])];
  if (tier === 1) return dedup(TIER_CHAR_POOLS.low1, themePool.slice(0, 10));
  if (tier === 2) return dedup(TIER_CHAR_POOLS.low2, themePool.slice(0, 15));
  if (tier === 3) return dedup(TIER_CHAR_POOLS.low3, themePool.slice(0, 18));
  if (tier === 4) return dedup(TIER_CHAR_POOLS.low4, themePool.slice(0, 20));
  if (tier === 5) return dedup(TIER_CHAR_POOLS.mid5, themePool.slice(0, 10));
  if (tier === 6) return dedup(TIER_CHAR_POOLS.mid6, themePool.slice(0, 30));
  if (tier === 7) return dedup(TIER_CHAR_POOLS.mid7, themePool.slice(0, 45));
  if (tier === 8) return dedup(TIER_CHAR_POOLS.mid8, themePool.slice(0, 55));
  if (tier <= 8)  return dedup(TIER_CHAR_POOLS.mid,  themePool.slice(0, 60));
  if (tier === 9)  return dedup(TIER_CHAR_POOLS.high9,  themePool.slice(0, 40));
  if (tier === 10) return dedup(TIER_CHAR_POOLS.high10, themePool.slice(0, 55));
  if (tier === 11) return dedup(TIER_CHAR_POOLS.high11, themePool.slice(0, 65));
  if (tier === 12) return dedup(TIER_CHAR_POOLS.high12, themePool);
  if (tier <= 12) return dedup(TIER_CHAR_POOLS.high,  themePool);
  if (tier === 13) return dedup(TIER_CHAR_POOLS.divine13, themePool.slice(-10));
  if (tier === 14) return dedup(TIER_CHAR_POOLS.divine14, themePool.slice(-15));
  if (tier === 15) return dedup(TIER_CHAR_POOLS.divine15, themePool.slice(-18));
  if (tier === 16) return dedup(TIER_CHAR_POOLS.divine16, themePool.slice(-20));
  return dedup(TIER_CHAR_POOLS.divine, themePool.slice(-20));
}

