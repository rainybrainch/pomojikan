/* レア度ごとに字体加飾度（テーマと組み合わさる） */
function rarityStyle(tier, theme) {
  const t = Math.max(1, Math.min(16, tier));
  const intensity = (t - 1) / 15; // 0..1
  return {
    glow: `0 0 ${4 + intensity * 18}px ${theme.glyphGlow}`,
    weight: t >= 12 ? 700 : t >= 8 ? 500 : 400,
    scale: 1 + intensity * 0.25,
  };
}

